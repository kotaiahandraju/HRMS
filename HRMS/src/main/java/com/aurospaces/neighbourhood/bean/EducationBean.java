package com.aurospaces.neighbourhood.bean;

public class EducationBean {
	
	private int id;
	private String empId;
	private String schoolName;
	private String qualification;
	private String qualifiedIn;
	private String schoolCompletionDate;
	private String additionalNotes;
	private String interests;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getEmpId() {
		return empId;
	}
	public void setEmpId(String empId) {
		this.empId = empId;
	}
	public String getSchoolName() {
		return schoolName;
	}
	public void setSchoolName(String schoolName) {
		this.schoolName = schoolName;
	}
	public String getQualification() {
		return qualification;
	}
	public void setQualification(String qualification) {
		this.qualification = qualification;
	}
	public String getQualifiedIn() {
		return qualifiedIn;
	}
	public void setQualifiedIn(String qualifiedIn) {
		this.qualifiedIn = qualifiedIn;
	}
	public String getSchoolCompletionDate() {
		return schoolCompletionDate;
	}
	public void setSchoolCompletionDate(String schoolCompletionDate) {
		this.schoolCompletionDate = schoolCompletionDate;
	}
	public String getAdditionalNotes() {
		return additionalNotes;
	}
	public void setAdditionalNotes(String additionalNotes) {
		this.additionalNotes = additionalNotes;
	}
	public String getInterests() {
		return interests;
	}
	public void setInterests(String interests) {
		this.interests = interests;
	}
}
